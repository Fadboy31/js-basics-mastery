// 🔹 Numbers 
let score = 42;
console.log("Score:", score);
console.log("Score + 10:", score + 10); // math works

// 🔹 Strings
let message = "Hello, World!";
console.log("Message:", message);

// String concatenation
let name = "MelekhFad";
console.log("Hello, " + name + "!"); // old style
console.log(`Hello, ${name}!`); // template literal (modern)

// 🔹 Booleans
let isLoggedIn = true;
let isAdmin = false;
console.log("isLoggedIn:", isLoggedIn);
console.log("isAdmin:", isAdmin);

// 🔹 Checking data types
console.log("Type of score:", typeof score); // number
console.log("Type of message:", typeof message); // string
console.log("Type of isLoggedIn:", typeof isLoggedIn); // boolean